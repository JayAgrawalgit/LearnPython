# Python supports two types of numbers - integers and floating point numbers. (It also supports complex numbers, which
# will not be explained in this tutorial).
# To define an integer, use the following syntax:
myint = 7
print("Integer Value printed:",myint)


# To define a floating point number, you may use one of the following notations:
myfloat = 7.0
print("Float Value printed:",myfloat)
myfloat = float(7)
print("Float Value printed:",myfloat)


# Assignments can be done on more than one variable "simultaneously" on the same line like this
a , b = 3 , 4
print("print a:",a)
print("print b:",b)
print("print a,b:", a,b)